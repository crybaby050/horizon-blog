    <!-- Corps article -->
    <div class="detail-body">
      <p>
        Depuis quelques années, l'intelligence artificielle s'impose comme un <strong>levier de transformation majeur</strong> sur le continent africain, capable de révolutionner des secteurs aussi variés que la santé, l'agriculture, l'éducation et la finance.
      </p>

      <h2>Un écosystème technologique en pleine effervescence</h2>
      <p>
        Des hubs technologiques émergent dans plusieurs grandes métropoles africaines — Nairobi, Lagos, Dakar, Le Caire — et attirent de plus en plus d'investisseurs internationaux. Ces espaces favorisent l'innovation locale et la formation de développeurs spécialisés en machine learning.
      </p>
      <p>
        En 2023, les investissements dans les startups africaines spécialisées en IA ont dépassé le milliard de dollars, un chiffre en hausse de 40 % par rapport à l'année précédente.
      </p>

      <blockquote class="detail-blockquote">
        <p>"L'Afrique ne doit pas seulement consommer l'intelligence artificielle, elle doit en être un acteur créateur, décisif et souverain."</p>
        <cite>— Dr. Moussa Diallo, chercheur en IA, UCAD</cite>
      </blockquote>

      <h2>Les défis structurels à surmonter</h2>
      <p>
        Malgré cet élan prometteur, des obstacles persistent. L'accès limité aux données de qualité en langues africaines freine le développement de modèles adaptés aux réalités locales. Les infrastructures cloud restent coûteuses et inégalement réparties.
      </p>

      <img src="https://images.unsplash.com/photo-1531482615713-2afd69097998?w=900&q=80" alt="Data center" class="detail-inline-img"/>

      <h3>La question de la souveraineté des données</h3>
      <p>
        Un enjeu fondamental émerge : à qui appartiennent les données produites par les populations africaines ? Comment s'assurer que les algorithmes entraînés sur ces données ne reproduisent pas des biais culturels ou socio-économiques préjudiciables ?
      </p>
